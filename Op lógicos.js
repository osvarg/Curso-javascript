let a = true;
let b = false;

console.log(a);
console.log(b);

//Operador logico && (and o Y)
// Regresa verdadero solo si ambos operandos son verdaderos
console.log(`${a} && ${b} -> ${a && b}`);

// Operador LÃ³gico || (or o O)
// Refresa verdadero si cualquiera de los valores es verdadero
console.log(`${a} || ${b} -> ${a || b}`);

// Operador ! (not o NO)
// Invierte el valor original
console.log(`${a} -> ${!a}`)